import mongoose from "mongoose";

//Create schema for model
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);//

//Create model using its created schema
const User = mongoose.model("User", userSchema);

export default User;
